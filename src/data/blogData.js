// Helper functions
export const getBlogBySlug = (slug) => {
  return blogPosts.find(blog => blog.slug === slug);
};

export const getRelatedBlogs = (currentId, category, limit = 3) => {
  return blogPosts
    .filter(blog => blog.id !== currentId && blog.category === category)
    .slice(0, limit);
};

export const getAllCategories = () => {
  const categories = blogPosts.map(blog => blog.category);
  return ['all', ...new Set(categories)];
};

export const getFeaturedBlogs = () => {
  return blogPosts.slice(0, 3);
};

export const searchBlogs = (keyword) => {
  if (!keyword) return blogPosts;
  const search = keyword.toLowerCase();
  return blogPosts.filter(blog =>
    blog.title.toLowerCase().includes(search) ||
    blog.shortDescription.toLowerCase().includes(search) ||
    blog.content.some(item => 
      item.text?.toLowerCase().includes(search)
    )
  );
};